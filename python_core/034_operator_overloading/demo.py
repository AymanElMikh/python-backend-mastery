print("Section 1: __add__ / __radd__ chain")
print("=" * 50)

class Money:
    def __init__(self, amount, currency="USD"):
        self.amount   = amount
        self.currency = currency

    def __add__(self, other):
        if isinstance(other, Money):
            if self.currency != other.currency:
                return NotImplemented   # signal: try other.__radd__
            return Money(self.amount + other.amount, self.currency)
        return NotImplemented

    def __radd__(self, other):
        if other == 0:            # sum() compatibility
            return self
        return NotImplemented

    def __repr__(self):
        return f"Money({self.amount} {self.currency})"

usd5  = Money(5, "USD")
usd10 = Money(10, "USD")
eur3  = Money(3, "EUR")

print(usd5 + usd10)             # Money(15 USD)
print(sum([usd5, usd10]))       # Money(15 USD) — radd(0) used

try:
    result = usd5 + eur3
    print(result)
except TypeError as e:
    print("Mixed currency:", e)

# ── Section 2: __iadd__ must return self ────────────────────────────────────
