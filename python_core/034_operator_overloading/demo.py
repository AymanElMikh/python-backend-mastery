print("=" * 50)

class NoHash:
    def __eq__(self, other):
        return True

try:
    d = {NoHash(): "oops"}
except TypeError as e:
    print("Unhashable:", e)

class WithHash:
    def __eq__(self, other):
        return True
    def __hash__(self):
        return 42

d = {WithHash(): "ok"}
print("Hashable dict:", d)

# ── Section 4: total_ordering ────────────────────────────────────────────────
print("\n" + "=" * 50)
print("Section 4: total_ordering")
print("=" * 50)

@total_ordering
class Version:
    def __init__(self, major, minor):
        self.t = (major, minor)
    def __eq__(self, other):
        return self.t == other.t
    def __lt__(self, other):
        return self.t < other.t
    def __repr__(self):
        return f"Version{self.t}"

v1 = Version(1, 2)
v2 = Version(1, 9)
v3 = Version(2, 0)
print(sorted([v3, v1, v2]))    # needs __lt__: should print ordered
print(v1 <= v2, v3 > v2)       # generated by total_ordering

