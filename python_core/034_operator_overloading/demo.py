class BadVec:
    def __init__(self, x):
        self.x = x
    def __iadd__(self, other):
        self.x += other.x
        # BUG: missing return self

class GoodVec:
    def __init__(self, x):
        self.x = x
    def __iadd__(self, other):
        self.x += other.x
        return self             # MUST return self

b = BadVec(1)
b += BadVec(2)
print("BadVec after +=:", b)    # None — rebind happened, lost the object!

g = GoodVec(1)
g += GoodVec(2)
print("GoodVec after +=:", g.x) # 3

# ── Section 3: __eq__ kills __hash__ ────────────────────────────────────────
